#알파벳 딕셔너리를 만들고
alpha = {'a': 0, 'b': 0, 'c': 0, 'd': 0, 'e': 0,
        'f': 0, 'g': 0, 'h': 0, 'i': 0, 'j': 0,
        'k': 0, 'l': 0, 'm': 0, 'n': 0, 'o': 0,
        'p': 0, 'q': 0, 'r': 0, 's': 0, 't': 0,
        'u': 0, 'v': 0, 'w': 0, 'x': 0, 'y': 0, 'z': 0}

s= input() #알파벳 입력받고

# 알파벳을 key로 하는 값을 1개씩 더하고
for i in s:
    alpha[i] +=1

# 딕셔너리 값을 하나씩 출력
for j in alpha.values():
    print(j, end=" ")